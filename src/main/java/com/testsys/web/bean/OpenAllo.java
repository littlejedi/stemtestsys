package com.testsys.web.bean;

public class OpenAllo {
	private int openAlloId;
	private String optionalOpenId;
	private String selectedOpenId;
	private String beginTime;
	private String course;
	private String answerFileSrc;
	public int getOpenAlloId() {
		return openAlloId;
	}
	public void setOpenAlloId(int openAlloId) {
		this.openAlloId = openAlloId;
	}
	public String getOptionalOpenId() {
		return optionalOpenId;
	}
	public void setOptionalOpenId(String optionalOpenId) {
		this.optionalOpenId = optionalOpenId;
	}
	public String getSelectedOpenId() {
		return selectedOpenId;
	}
	public void setSelectedOpenId(String selectedOpenId) {
		this.selectedOpenId = selectedOpenId;
	}
	public String getBeginTime() {
		return beginTime;
	}
	public void setBeginTime(String beginTime) {
		this.beginTime = beginTime;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public String getAnswerFileSrc() {
		return answerFileSrc;
	}
	public void setAnswerFileSrc(String answerFileSrc) {
		this.answerFileSrc = answerFileSrc;
	}
}	
