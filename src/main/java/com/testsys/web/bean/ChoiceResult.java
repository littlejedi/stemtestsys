package com.testsys.web.bean;

public class ChoiceResult {
	private double choices;
	private double choicet;
	private double choicee;
	private double choicem;
	private int choicescore;
	private int status;
	private String createtime;
	public double getChoices() {
		return choices;
	}
	public void setChoices(double choices) {
		this.choices = choices;
	}
	public double getChoicet() {
		return choicet;
	}
	public void setChoicet(double choicet) {
		this.choicet = choicet;
	}
	public double getChoicee() {
		return choicee;
	}
	public void setChoicee(double choicee) {
		this.choicee = choicee;
	}
	public double getChoicem() {
		return choicem;
	}
	public void setChoicem(double choicem) {
		this.choicem = choicem;
	}
	public int getChoicescore() {
		return choicescore;
	}
	public void setChoicescore(int choicescore) {
		this.choicescore = choicescore;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getCreatetime() {
		return createtime;
	}
	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}
}
